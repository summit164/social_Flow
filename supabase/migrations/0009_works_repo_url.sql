-- ============================================================
-- 0009 — Ссылка на репозиторий GitHub у артефактов
-- ============================================================
-- Опциональное поле для артефактов: ссылка на репозиторий GitHub.
-- На странице артефакта по этой ссылке подтянутся данные через
-- публичный GitHub API (звёзды, язык, кусок README) и нарисуется
-- интерактивная карточка.
-- ============================================================

alter table public.works
  add column repo_url text;

-- Мягкая валидация формата (полная — на уровне приложения)
alter table public.works
  add constraint works_repo_url_format
  check (
    repo_url is null
    or repo_url ~* '^https?://(www\.)?github\.com/[^/]+/[^/]+/?'
  );
